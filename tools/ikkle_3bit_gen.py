PLACEHOLDER = {35, 36, 37, 38, 64, 127}  # #$%&@ DEL
# Remap: use 0xEE,0xAA,0xAA,0xEE (like '0'/'O') for placeholders
PLACEHOLDER_NIBS = (0xE, 0xA, 0xA, 0xE)

# Remap ~: change 0x5 → 0x6 (one pixel diff in scanline 1)
TILDE_NIBS = (0x0, 0x6, 0xA, 0x0)  # was (0, 5, A, 0)

def get_nibs(ch):
    if ch in PLACEHOLDER:
        return PLACEHOLDER_NIBS
    if ch == 126:  # ~
        return TILDE_NIBS
    i = ch - 32
    return tuple((ikkle_raw[i*5+s] >> 4) & 0xF for s in range(4))

def nib_art(n):
    return ''.join('#' if (n >> (3-i)) & 1 else '.' for i in range(4))

# Verify all nibbles are even
print("Verifying all nibbles are even after remapping...")
all_even = True
for ch in range(32, 128):
    for n in get_nibs(ch):
        if n & 1:
            print(f"  ODD nibble 0x{n:X} in char {ch} ('{chr(ch)}')")
            all_even = False
print(f"{'ALL EVEN' if all_even else 'ERRORS FOUND'}\n")

# Generate 3-bit codes: code = nibble >> 1
def get_codes(ch):
    return tuple(n >> 1 for n in get_nibs(ch))

# Pack 2 chars per 3 bytes:
# Char A codes: a0 a1 a2 a3 (each 3 bits)
# Char B codes: b0 b1 b2 b3
#
# byte0 = a0<<5 | a1<<2 | a2>>1
# byte1 = (a2&1)<<7 | a3<<4 | b0<<1 | b1>>2
# byte2 = (b1&3)<<6 | b2<<3 | b3

packed_3bit = []
for pair in range(48):  # 96 chars / 2
    ch_a = 32 + pair * 2
    ch_b = 33 + pair * 2
    a = get_codes(ch_a)
    b = get_codes(ch_b)

    byte0 = (a[0] << 5) | (a[1] << 2) | (a[2] >> 1)
    byte1 = ((a[2] & 1) << 7) | (a[3] << 4) | (b[0] << 1) | (b[1] >> 2)
    byte2 = ((b[1] & 3) << 6) | (b[2] << 3) | b[3]

    packed_3bit.extend([byte0, byte1, byte2])

    # Verify roundtrip
    ra0 = (byte0 >> 5) & 7
    ra1 = (byte0 >> 2) & 7
    ra2 = ((byte0 & 3) << 1) | ((byte1 >> 7) & 1)
    ra3 = (byte1 >> 4) & 7
    rb0 = (byte1 >> 1) & 7
    rb1 = ((byte1 & 1) << 2) | ((byte2 >> 6) & 3)
    rb2 = (byte2 >> 3) & 7
    rb3 = byte2 & 7

    assert (ra0, ra1, ra2, ra3) == a, f"Roundtrip fail char {ch_a}: {(ra0,ra1,ra2,ra3)} != {a}"
    assert (rb0, rb1, rb2, rb3) == b, f"Roundtrip fail char {ch_b}: {(rb0,rb1,rb2,rb3)} != {b}"

print(f"3-bit packed: {len(packed_3bit)} bytes (vs 192 nibble-packed)")
print(f"Savings: {192 - len(packed_3bit)} bytes of data\n")

# Show visual diff for affected chars
print("Modified characters:")
for ch in sorted(PLACEHOLDER | {126}):
    orig = tuple((ikkle_raw[(ch-32)*5+s] >> 4) & 0xF for s in range(4))
    new = get_nibs(ch)
    if orig != new:
        orig_art = ' '.join(nib_art(n) for n in orig)
        new_art = ' '.join(nib_art(n) for n in new)
        print(f"  '{chr(ch) if ch < 127 else 'DEL'}' ({ch}): {orig_art} → {new_art}")

# Output as Z80 DEFB
print(f"\n; Ikkle-4 font, 3-bit packed, 144 bytes")
print(f"; Placeholders remapped to even-only, ~ adjusted (0x5→0x6)")
print(f"; Pack: 2 chars per 3 bytes. code = nibble >> 1 (3 bits)")
print(f"; Decode: nibble = code << 1, display = nibble * 0x11")
print(f"; Even char (A): a0=byte0[7:5] a1=byte0[4:2] a2=byte0[1:0]:byte1[7] a3=byte1[6:4]")
print(f"; Odd char  (B): b0=byte1[3:1] b1=byte1[0]:byte2[7:6] b2=byte2[5:3] b3=byte2[2:0]")
print("ikkle_3bit:")
for row in range(0, len(packed_3bit), 12):
    chunk = packed_3bit[row:row+12]
    hexs = ','.join(f'0x{b:02X}' for b in chunk)
    print(f"    DEFB {hexs}")

# Also generate the old nibble-packed for comparison (with same remapping)
